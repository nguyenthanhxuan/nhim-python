# Variables are like boxes that store information
# We can put different types of information in them

# Let's create some variables!
name = "Alex"           # This stores text (called a string)
age = 12               # This stores a number (called an integer)
height = 4.5           # This stores a decimal number (called a float)
likes_pizza = True     # This stores True or False (called a boolean)

# Now let's use our variables!
print("My name is", name)
print("I am", age, "years old")
print("I am", height, "feet tall")
print("I like pizza:", likes_pizza)

# We can change what's stored in a variable
age = 13  # Happy birthday!
print("Now I am", age, "years old")

# We can also do math with variables
next_year = age + 1
print("Next year I will be", next_year)

# Let's ask the user for their information
user_name = input("What's your name? ")
user_age = input("How old are you? ")

print("Nice to meet you,", user_name + "!")
print("You are", user_age, "years old")

